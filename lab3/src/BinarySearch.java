/**
 * Класс, реализующий метод бинарного поиска.
 */
public class BinarySearch {

    // Конструктор по умолчанию
    public BinarySearch() {
    }

    /**
     * Выполняет бинарный поиск заданного значения в массиве.
     *
     * @param array отсортированный массив целых чисел
     * @param target искомое значение
     * @return индекс элемента, если он найден; иначе -1
     */
    public int binarySearch(int[] array, int target) {
        int left = 0;                   // Начальная граница
        int right = array.length - 1;  // Конечная граница

        // Выполняем поиск, пока левая граница меньше или равна правой
        while (left <= right) {
            // Вычисляем середину с учетом возможного переполнения
            int mid = left + (right - left) / 2;

            // Если элемент в середине равен целевому, возвращаем его индекс
            if (array[mid] == target) {
                return mid;
            }

            // Если средний элемент меньше целевого, смещаем левую границу
            if (array[mid] < target) {
                left = mid + 1;
            } 
            // Если средний элемент больше целевого, смещаем правую границу
            else {
                right = mid - 1;
            }
        }

        // Если элемент не найден, возвращаем -1
        return -1;
    }
}
